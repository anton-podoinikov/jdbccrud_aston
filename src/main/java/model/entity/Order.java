package model.entity;

import lombok.Data;

import java.util.ArrayList;
import java.util.List;

/**
 * Класс Order представляет сущность заказа в системе.
 * Этот класс содержит информацию о заказе, включая его идентификатор, пользователя, сделавшего заказ,
 * и список продуктов, включенных в заказ.
 *
 * Использование аннотации @Data из библиотеки Lombok позволяет автоматически генерировать
 * геттеры, сеттеры и методы hashCode(), equals() и toString() для всех полей класса,
 * уменьшая объем кода и упрощая поддержку.
 */
@Data
public class Order {
    /**
     * Идентификатор заказа, уникальный ключ в базе данных.
     */
    private int id;

    /**
     * Пользователь, сделавший заказ. Связь между заказом и пользователем позволяет отслеживать,
     * кто совершил покупку.
     */
    private User user;

    /**
     * Список продуктов, включенных в заказ. Позволяет управлять содержимым заказа и проводить анализ.
     */
    private List<Product> products;

    /**
     * Конструктор по умолчанию для создания пустого заказа.
     */
    public Order() {
        this.products = new ArrayList<>();
    }

    /**
     * Конструктор для создания заказа с указанным идентификатором.
     * Инициализирует пустой список продуктов для последующего добавления в заказ.
     *
     * @param id Идентификатор создаваемого заказа.
     */
    public Order(int id) {
        this.id = id;
        this.products = new ArrayList<>();
    }
}
